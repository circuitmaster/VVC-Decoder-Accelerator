static inline TCoeff clip3(TCoeff lo, TCoeff hi, TCoeff v) {
    return v < lo ? lo : v > hi ? hi : v;
}

static inline uint8_t clip_u8(TCoeff v) {
    return static_cast<uint8_t>(v < 0 ? 0 : v > 255 ? 255 : v);
}

// ---------------------------------------------------------------------------
// Forward 1-D DCT-2 64-point  (butterfly decomposition)
//
//   src   : 64 input samples (pixels or intermediate values)
//   dst   : 64 output coefficients
//   shift : right-shift (with rounding) applied after accumulation
//
// The butterfly implicitly computes T^T * src where T is the idct64 matrix,
// i.e. it performs the forward DCT-2.  No explicit matrix is needed here.
// ---------------------------------------------------------------------------

static void forward_dct2_64(const TCoeff* src, TCoeff* dst, int shift)
{
    const int rnd = 1 << (shift - 1);

    TCoeff E[32], O[32];
    TCoeff EE[16], EO[16];
    TCoeff EEE[8], EEO[8];
    TCoeff EEEE[4], EEEO[4];
    TCoeff EEEEE[2], EEEEO[2];

    // --- Stage 1: even/odd split ---
    for (int k = 0; k < 32; ++k) {
        E[k] = src[k] + src[63 - k];
        O[k] = src[k] - src[63 - k];
    }
    for (int k = 0; k < 16; ++k) {
        EE[k] = E[k] + E[31 - k];
        EO[k] = E[k] - E[31 - k];
    }
    for (int k = 0; k < 8; ++k) {
        EEE[k] = EE[k] + EE[15 - k];
        EEO[k] = EE[k] - EE[15 - k];
    }
    for (int k = 0; k < 4; ++k) {
        EEEE[k] = EEE[k] + EEE[7 - k];
        EEEO[k] = EEE[k] - EEE[7 - k];
    }
    EEEEE[0] = EEEE[0] + EEEE[3];
    EEEEO[0] = EEEE[0] - EEEE[3];
    EEEEE[1] = EEEE[1] + EEEE[2];
    EEEEO[1] = EEEE[1] - EEEE[2];

    // --- DC/Nyquist (multiples of 32) using idct64 row 0, 16, 32, 48 ---
    dst[ 0] = (idct64[ 0][0]*EEEEE[0] + idct64[ 0][1]*EEEEE[1] + rnd) >> shift;
    dst[16] = (idct64[16][0]*EEEEO[0] + idct64[16][1]*EEEEO[1] + rnd) >> shift;
    dst[32] = (idct64[32][0]*EEEEE[0] + idct64[32][1]*EEEEE[1] + rnd) >> shift;
    dst[48] = (idct64[48][0]*EEEEO[0] + idct64[48][1]*EEEEO[1] + rnd) >> shift;

    // --- Multiples of 8 (not 16): rows 8, 24, 40, 56 ---
    for (int k = 8; k < 64; k += 16)
        dst[k] = (idct64[k][0]*EEEO[0] + idct64[k][1]*EEEO[1] +
                  idct64[k][2]*EEEO[2] + idct64[k][3]*EEEO[3] + rnd) >> shift;

    // --- Multiples of 4 (not 8): rows 4, 12, 20, 28, 36, 44, 52, 60 ---
    for (int k = 4; k < 64; k += 8)
        dst[k] = (idct64[k][0]*EEO[0] + idct64[k][1]*EEO[1] +
                  idct64[k][2]*EEO[2] + idct64[k][3]*EEO[3] +
                  idct64[k][4]*EEO[4] + idct64[k][5]*EEO[5] +
                  idct64[k][6]*EEO[6] + idct64[k][7]*EEO[7] + rnd) >> shift;

    // --- Multiples of 2 (not 4): rows 2, 6, 10, ... 62 ---
    for (int k = 2; k < 64; k += 4)
        dst[k] = (idct64[k][ 0]*EO[ 0] + idct64[k][ 1]*EO[ 1] +
                  idct64[k][ 2]*EO[ 2] + idct64[k][ 3]*EO[ 3] +
                  idct64[k][ 4]*EO[ 4] + idct64[k][ 5]*EO[ 5] +
                  idct64[k][ 6]*EO[ 6] + idct64[k][ 7]*EO[ 7] +
                  idct64[k][ 8]*EO[ 8] + idct64[k][ 9]*EO[ 9] +
                  idct64[k][10]*EO[10] + idct64[k][11]*EO[11] +
                  idct64[k][12]*EO[12] + idct64[k][13]*EO[13] +
                  idct64[k][14]*EO[14] + idct64[k][15]*EO[15] + rnd) >> shift;

    // --- Odd rows: 1, 3, 5, ... 63 ---
    for (int k = 1; k < 64; k += 2)
        dst[k] = (idct64[k][ 0]*O[ 0] + idct64[k][ 1]*O[ 1] +
                  idct64[k][ 2]*O[ 2] + idct64[k][ 3]*O[ 3] +
                  idct64[k][ 4]*O[ 4] + idct64[k][ 5]*O[ 5] +
                  idct64[k][ 6]*O[ 6] + idct64[k][ 7]*O[ 7] +
                  idct64[k][ 8]*O[ 8] + idct64[k][ 9]*O[ 9] +
                  idct64[k][10]*O[10] + idct64[k][11]*O[11] +
                  idct64[k][12]*O[12] + idct64[k][13]*O[13] +
                  idct64[k][14]*O[14] + idct64[k][15]*O[15] +
                  idct64[k][16]*O[16] + idct64[k][17]*O[17] +
                  idct64[k][18]*O[18] + idct64[k][19]*O[19] +
                  idct64[k][20]*O[20] + idct64[k][21]*O[21] +
                  idct64[k][22]*O[22] + idct64[k][23]*O[23] +
                  idct64[k][24]*O[24] + idct64[k][25]*O[25] +
                  idct64[k][26]*O[26] + idct64[k][27]*O[27] +
                  idct64[k][28]*O[28] + idct64[k][29]*O[29] +
                  idct64[k][30]*O[30] + idct64[k][31]*O[31] + rnd) >> shift;
}

// ---------------------------------------------------------------------------
// Inverse 1-D DCT-2 64-point  (uses idct64 table directly)
//
//   src    : 64 transform coefficients  (output of forward_dct2_64)
//   dst    : 64 reconstructed samples
//   shift  : right-shift (with rounding) applied after accumulation
//   outMin / outMax : clip range applied to each output sample
//
// The inverse is:  dst[n] = sum_k  idct64[k][n] * src[k]
// i.e. column n of idct64 dot-producted with the coefficient vector.
// ---------------------------------------------------------------------------

static void inverse_dct2_64(const TCoeff* src, TCoeff* dst, int shift,
                             TCoeff outMin, TCoeff outMax)
{
    int64_t rnd = 1LL << (shift - 1);
    TCoeff E[32],O[32],EE[16],EO[16],EEE[8],EEO[8],EEEE[4],EEEO[4],EEEEE[2],EEEEO[2];

    for (int k = 0; k < 32; k++)
        O[k] = idct64[ 1][k]*src[ 1] + idct64[ 3][k]*src[ 3] + idct64[ 5][k]*src[ 5] + idct64[ 7][k]*src[ 7] +
               idct64[ 9][k]*src[ 9] + idct64[11][k]*src[11] + idct64[13][k]*src[13] + idct64[15][k]*src[15] +
               idct64[17][k]*src[17] + idct64[19][k]*src[19] + idct64[21][k]*src[21] + idct64[23][k]*src[23] +
               idct64[25][k]*src[25] + idct64[27][k]*src[27] + idct64[29][k]*src[29] + idct64[31][k]*src[31] +
               idct64[33][k]*src[33] + idct64[35][k]*src[35] + idct64[37][k]*src[37] + idct64[39][k]*src[39] +
               idct64[41][k]*src[41] + idct64[43][k]*src[43] + idct64[45][k]*src[45] + idct64[47][k]*src[47] +
               idct64[49][k]*src[49] + idct64[51][k]*src[51] + idct64[53][k]*src[53] + idct64[55][k]*src[55] +
               idct64[57][k]*src[57] + idct64[59][k]*src[59] + idct64[61][k]*src[61] + idct64[63][k]*src[63];

    for (int k = 0; k < 16; k++)
        EO[k] = idct64[ 2][k]*src[ 2] + idct64[ 6][k]*src[ 6] + idct64[10][k]*src[10] + idct64[14][k]*src[14] +
                idct64[18][k]*src[18] + idct64[22][k]*src[22] + idct64[26][k]*src[26] + idct64[30][k]*src[30] +
                idct64[34][k]*src[34] + idct64[38][k]*src[38] + idct64[42][k]*src[42] + idct64[46][k]*src[46] +
                idct64[50][k]*src[50] + idct64[54][k]*src[54] + idct64[58][k]*src[58] + idct64[62][k]*src[62];

    for (int k = 0; k < 8; k++)
        EEO[k] = idct64[ 4][k]*src[ 4] + idct64[12][k]*src[12] + idct64[20][k]*src[20] + idct64[28][k]*src[28] +
                 idct64[36][k]*src[36] + idct64[44][k]*src[44] + idct64[52][k]*src[52] + idct64[60][k]*src[60];

    for (int k = 0; k < 4; k++)
        EEEO[k] = idct64[8][k]*src[8] + idct64[24][k]*src[24] + idct64[40][k]*src[40] + idct64[56][k]*src[56];

    EEEEO[0] = idct64[16][0]*src[16] + idct64[48][0]*src[48];
    EEEEO[1] = idct64[16][1]*src[16] + idct64[48][1]*src[48];
    EEEEE[0] = idct64[ 0][0]*src[ 0] + idct64[32][0]*src[32];
    EEEEE[1] = idct64[ 0][1]*src[ 0] + idct64[32][1]*src[32];

    for (int k = 0; k < 2; k++) { EEEE[k] = EEEEE[k] + EEEEO[k]; EEEE[k+2] = EEEEE[1-k] - EEEEO[1-k]; }
    for (int k = 0; k < 4; k++) { EEE[k]  = EEEE[k]  + EEEO[k];   EEE[k+4]  = EEEE[3-k]  - EEEO[3-k]; }
    for (int k = 0; k < 8; k++) { EE[k]   = EEE[k]   + EEO[k];    EE[k+8]   = EEE[7-k]   - EEO[7-k];  }
    for (int k = 0; k < 16; k++){ E[k]    = EE[k]    + EO[k];     E[k+16]   = EE[15-k]   - EO[15-k];  }
    for (int k = 0; k < 32; k++){
        dst[k]    = clip3(outMin, outMax, (TCoeff)(((int64_t)E[k]      + O[k]      + rnd) >> shift));
        dst[k+32] = clip3(outMin, outMax, (TCoeff)(((int64_t)E[31-k]   - O[31-k]   + rnd) >> shift));
    }
}
// ---------------------------------------------------------------------------
// 2-D forward transform: 64x64 block
//   Separable: horizontal 1-D pass  →  vertical 1-D pass
//
//   pixels_in  : 64*64 uint8 luma samples, row-major
//   coeffs_out : 64*64 TCoeff transform coefficients, row-major
// ---------------------------------------------------------------------------

static void transform_block_2d(const uint8_t* pixels_in, TCoeff* coeffs_out)
{
    // Shift values match the VVC reference:
    //   horizontal pass: log2(N) + bitDepth - 1 - FILTER_PREC  →  6+8-1-6 = 7
    //   vertical   pass: log2(N) + 6  - bitDepth              →  6+6-8   = ... 
    // Conventional choice used here (matches original code):
    //   shift_h = 7,  shift_v = 12
    const int shift_h = 6;
    const int shift_v = 6;

    // --- Horizontal pass: transform each row ---
    TCoeff tmp[64 * 64];
    TCoeff row_in[64], row_out[64];

    for (int r = 0; r < 64; ++r) {
        for (int c = 0; c < 64; ++c)
            row_in[c] = static_cast<TCoeff>(pixels_in[r * 64 + c]);
        forward_dct2_64(row_in, row_out, shift_h);
        for (int c = 0; c < 64; ++c)
            tmp[r * 64 + c] = row_out[c];
    }

    // --- Vertical pass: transform each column of the horizontal result ---
    TCoeff col_in[64], col_out[64];

    for (int c = 0; c < 64; ++c) {
        for (int r = 0; r < 64; ++r)
            col_in[r] = tmp[r * 64 + c];
        forward_dct2_64(col_in, col_out, shift_v);
        for (int r = 0; r < 64; ++r)
            coeffs_out[r * 64 + c] = col_out[r];
    }
}

// ---------------------------------------------------------------------------
// 2-D inverse transform: 64x64 block
//   Separable: vertical 1-D pass  →  horizontal 1-D pass
//   (mirror of forward: vertical first to undo the last forward pass)
//
//   coeffs_in  : 64*64 TCoeff coefficients — direct output of transform_block_2d
//   pixels_out : 64*64 uint8 reconstructed luma samples, row-major
// ---------------------------------------------------------------------------

static void itransform_block_2d(const TCoeff* coeffs_in, uint8_t* pixels_out)
{
    // shift_v (first inverse pass) undoes the forward vertical shift
    // shift_h (second inverse pass) undoes the forward horizontal shift
    // plus restores pixel range → clip to [0, 255] at the end
    const int shift_v = 12;
    const int shift_h = 12;

    // --- Vertical inverse pass: transform each column ---
    TCoeff tmp[64 * 64];
    TCoeff col_in[64], col_out[64];

    for (int c = 0; c < 64; ++c) {
        for (int r = 0; r < 64; ++r)
            col_in[r] = coeffs_in[r * 64 + c];
        inverse_dct2_64(col_in, col_out, shift_v, -131072, 131072);
        for (int r = 0; r < 64; ++r)
            tmp[r * 64 + c] = col_out[r];
    }

    // --- Horizontal inverse pass: transform each row ---
    TCoeff row_in[64], row_out[64];

    for (int r = 0; r < 64; ++r) {
        for (int c = 0; c < 64; ++c)
            row_in[c] = tmp[r * 64 + c];
        inverse_dct2_64(row_in, row_out, shift_h, 0, 255);
        for (int c = 0; c < 64; ++c)
            pixels_out[r * 64 + c] = static_cast<uint8_t>(row_out[c]);
    }
}

// ---------------------------------------------------------------------------
// YUV planar 4:2:0 I/O  (luma transformed; chroma passed through unchanged)
// ---------------------------------------------------------------------------

struct Frame {
    int width  = 0;
    int height = 0;
    std::vector<uint8_t> luma;   // width * height
    std::vector<uint8_t> chroma; // width * height / 2  (Cb interleaved Cr)
};

static Frame read_yuv_frame(std::ifstream& f, int width, int height) {
    const size_t luma_sz   = static_cast<size_t>(width) * height;
    const size_t chroma_sz = luma_sz / 2;
    Frame fr;
    fr.width  = width;
    fr.height = height;
    fr.luma.resize(luma_sz);
    fr.chroma.resize(chroma_sz);
    f.read(reinterpret_cast<char*>(fr.luma.data()),   static_cast<std::streamsize>(luma_sz));
    f.read(reinterpret_cast<char*>(fr.chroma.data()), static_cast<std::streamsize>(chroma_sz));
    return fr;
}

static void write_yuv_frame(std::ofstream& f, const Frame& fr) {
    f.write(reinterpret_cast<const char*>(fr.luma.data()),
            static_cast<std::streamsize>(fr.luma.size()));
    f.write(reinterpret_cast<const char*>(fr.chroma.data()),
            static_cast<std::streamsize>(fr.chroma.size()));
}

// ---------------------------------------------------------------------------
// Coefficient file I/O
// Format: raw int32_t little-endian, row-major per block, blocks in raster order.
// All frames are concatenated.  The inverse path reads this file directly.
// ---------------------------------------------------------------------------

static void write_coeffs(std::ofstream& f, const std::vector<TCoeff>& coeffs) {
    f.write(reinterpret_cast<const char*>(coeffs.data()),
            static_cast<std::streamsize>(coeffs.size() * sizeof(TCoeff)));
}

static std::vector<TCoeff> read_coeffs(std::ifstream& f, size_t count) {
    std::vector<TCoeff> coeffs(count);
    f.read(reinterpret_cast<char*>(coeffs.data()),
           static_cast<std::streamsize>(count * sizeof(TCoeff)));
    return coeffs;
}

// ---------------------------------------------------------------------------
// Encode one luma plane: tile into 64x64 blocks and forward-transform each
// ---------------------------------------------------------------------------

static std::vector<TCoeff> encode_luma(const Frame& fr)
{
    const int W  = fr.width;
    const int H  = fr.height;
    const int bw = (W + 63) / 64;
    const int bh = (H + 63) / 64;

    std::vector<TCoeff> all_coeffs(static_cast<size_t>(bw * bh) * 64 * 64, 0);
    uint8_t block_pixels[64 * 64]{};
    TCoeff  block_coeffs[64 * 64]{};

    for (int by = 0; by < bh; ++by) {
        for (int bx = 0; bx < bw; ++bx) {
            // Extract 64x64 block, zero-padding at frame edges
            std::memset(block_pixels, 0, sizeof(block_pixels));
            for (int r = 0; r < 64; ++r) {
                for (int c = 0; c < 64; ++c) {
                    const int py = by * 64 + r;
                    const int px = bx * 64 + c;
                    if (py < H && px < W)
                        block_pixels[r * 64 + c] = fr.luma[py * W + px];
                }
            }

            // 2-D forward DCT-2 (horizontal then vertical)
            transform_block_2d(block_pixels, block_coeffs);

            const size_t offset = static_cast<size_t>(by * bw + bx) * 64 * 64;
            std::memcpy(all_coeffs.data() + offset, block_coeffs, sizeof(block_coeffs));
        }
    }
    return all_coeffs;
}

// ---------------------------------------------------------------------------
// Decode one luma plane: inverse-transform each block and reassemble
// ---------------------------------------------------------------------------

static Frame decode_luma(const std::vector<TCoeff>& all_coeffs,
                          int width, int height,
                          const std::vector<uint8_t>& chroma)
{
    const int bw = (width  + 63) / 64;
    const int bh = (height + 63) / 64;

    Frame out;
    out.width  = width;
    out.height = height;
    out.luma.assign(static_cast<size_t>(width) * height, 0);
    out.chroma = chroma;

    TCoeff  block_coeffs[64 * 64]{};
    uint8_t block_pixels[64 * 64]{};

    for (int by = 0; by < bh; ++by) {
        for (int bx = 0; bx < bw; ++bx) {
            const size_t offset = static_cast<size_t>(by * bw + bx) * 64 * 64;
            std::memcpy(block_coeffs, all_coeffs.data() + offset, sizeof(block_coeffs));

            // 2-D inverse DCT-2 (vertical then horizontal — mirrors forward)
            itransform_block_2d(block_coeffs, block_pixels);

            for (int r = 0; r < 64; ++r) {
                for (int c = 0; c < 64; ++c) {
                    const int py = by * 64 + r;
                    const int px = bx * 64 + c;
                    if (py < height && px < width)
                        out.luma[py * width + px] = block_pixels[r * 64 + c];
                }
            }
        }
    }
    return out;
}

// ---------------------------------------------------------------------------
// main
// ---------------------------------------------------------------------------
//
//  Two modes:
//
//  Encode + decode (full pipeline):
//    dct2_pipeline encode <input.yuv> <width> <height> [<num_frames>|-1]
//                         <transformed.bin> <reconstructed.yuv>
//
//  Decode only (inverse from saved coefficients):
//    dct2_pipeline decode <transformed.bin> <width> <height> [<num_frames>|-1]
//                         <reconstructed.yuv>
//
//  The .bin format is identical in both cases: raw int32_t LE, row-major
//  per 64x64 block, blocks in raster order, frames concatenated.
//  Chroma is not stored in the .bin; the decode-only mode emits grey chroma.
// ---------------------------------------------------------------------------

int main(int argc, char** argv)
{
    if (argc < 2) {
        std::cerr << "Usage:\n"
                  << "  Encode+decode: " << argv[0]
                  << " encode <in.yuv> <W> <H> [<frames>|-1] <out.bin> <recon.yuv>\n"
                  << "  Decode only:   " << argv[0]
                  << " decode <in.bin> <W> <H> [<frames>|-1] <recon.yuv>\n";
        return EXIT_FAILURE;
    }

    const std::string mode = argv[1];

    // -----------------------------------------------------------------------
    // ENCODE + DECODE mode
    // -----------------------------------------------------------------------
    if (mode == "encode") {
        if (argc < 8) {
            std::cerr << "encode needs: <in.yuv> <W> <H> <frames> <out.bin> <recon.yuv>\n";
            return EXIT_FAILURE;
        }
        const std::string input_yuv  = argv[2];
        const int W                  = std::stoi(argv[3]);
        const int H                  = std::stoi(argv[4]);
        const int frames_arg         = std::stoi(argv[5]);
        const std::string bin_path   = argv[6];
        const std::string recon_path = argv[7];

        if (W <= 0 || W % 2) { std::cerr << "W must be > 0 and even\n"; return EXIT_FAILURE; }
        if (H <= 0 || H % 2) { std::cerr << "H must be > 0 and even\n"; return EXIT_FAILURE; }

        std::ifstream fin(input_yuv, std::ios::binary | std::ios::ate);
        if (!fin) { std::cerr << "Cannot open: " << input_yuv << "\n"; return EXIT_FAILURE; }

        const size_t frame_bytes = static_cast<size_t>(W) * H * 3 / 2;
        const int    total_in    = static_cast<int>(static_cast<size_t>(fin.tellg()) / frame_bytes);
        const int    frames      = (frames_arg <= 0) ? total_in : std::min(frames_arg, total_in);
        fin.seekg(0);

        std::ofstream fbin(bin_path,   std::ios::binary);
        std::ofstream frec(recon_path, std::ios::binary);
        if (!fbin) { std::cerr << "Cannot write: " << bin_path   << "\n"; return EXIT_FAILURE; }
        if (!frec) { std::cerr << "Cannot write: " << recon_path << "\n"; return EXIT_FAILURE; }

        std::cout << "Mode:       encode\n"
                  << "Input:      " << input_yuv  << "\n"
                  << "Resolution: " << W << "x" << H << "\n"
                  << "Frames:     " << frames << " / " << total_in << "\n"
                  << "Coeffs:     " << bin_path   << "\n"
                  << "Recon:      " << recon_path  << "\n\n";

        for (int fi = 0; fi < frames; ++fi) {
            std::cout << "\rFrame " << (fi + 1) << " / " << frames << "  " << std::flush;

            Frame f = read_yuv_frame(fin, W, H);

            // Forward 2-D DCT-2: horizontal then vertical
            std::vector<TCoeff> coeffs = encode_luma(f);
            write_coeffs(fbin, coeffs);

            // Inverse 2-D DCT-2: vertical then horizontal — uses saved coeffs directly
            Frame recon = decode_luma(coeffs, W, H, f.chroma);
            write_yuv_frame(frec, recon);
        }
        std::cout << "\nDone.\n";

    // -----------------------------------------------------------------------
    // DECODE-ONLY mode  (reads .bin written by encode mode above)
    // -----------------------------------------------------------------------
    } else if (mode == "decode") {
        if (argc < 7) {
            std::cerr << "decode needs: <in.bin> <W> <H> <frames> <recon.yuv>\n";
            return EXIT_FAILURE;
        }
        const std::string bin_path   = argv[2];
        const int W                  = std::stoi(argv[3]);
        const int H                  = std::stoi(argv[4]);
        const int frames_arg         = std::stoi(argv[5]);
        const std::string recon_path = argv[6];

        if (W <= 0 || W % 2) { std::cerr << "W must be > 0 and even\n"; return EXIT_FAILURE; }
        if (H <= 0 || H % 2) { std::cerr << "H must be > 0 and even\n"; return EXIT_FAILURE; }

        std::ifstream fbin(bin_path, std::ios::binary | std::ios::ate);
        if (!fbin) { std::cerr << "Cannot open: " << bin_path << "\n"; return EXIT_FAILURE; }

        const int bw           = (W + 63) / 64;
        const int bh           = (H + 63) / 64;
        const size_t coeffs_per_frame = static_cast<size_t>(bw * bh) * 64 * 64;
        const size_t bytes_per_frame  = coeffs_per_frame * sizeof(TCoeff);
        const int    total_in  = static_cast<int>(static_cast<size_t>(fbin.tellg()) / bytes_per_frame);
        const int    frames    = (frames_arg <= 0) ? total_in : std::min(frames_arg, total_in);
        fbin.seekg(0);

        std::ofstream frec(recon_path, std::ios::binary);
        if (!frec) { std::cerr << "Cannot write: " << recon_path << "\n"; return EXIT_FAILURE; }

        // Grey chroma (128 = neutral Cb/Cr for 8-bit)
        const size_t chroma_sz = static_cast<size_t>(W) * H / 2;
        std::vector<uint8_t> grey_chroma(chroma_sz, 128);

        std::cout << "Mode:       decode\n"
                  << "Input:      " << bin_path   << "\n"
                  << "Resolution: " << W << "x" << H << "\n"
                  << "Frames:     " << frames << " / " << total_in << "\n"
                  << "Recon:      " << recon_path  << "\n\n";

        for (int fi = 0; fi < frames; ++fi) {
            std::cout << "\rFrame " << (fi + 1) << " / " << frames << "  " << std::flush;

            std::vector<TCoeff> coeffs = read_coeffs(fbin, coeffs_per_frame);

            // Inverse 2-D DCT-2: vertical then horizontal
            Frame recon = decode_luma(coeffs, W, H, grey_chroma);
            write_yuv_frame(frec, recon);
        }
        std::cout << "\nDone.\n";

    } else {
        std::cerr << "Unknown mode '" << mode << "'. Use 'encode' or 'decode'.\n";
        return EXIT_FAILURE;
    }

    return EXIT_SUCCESS;
}